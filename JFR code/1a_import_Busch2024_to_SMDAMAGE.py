# Execute one country pipeline from input .dta to SMDAMAGE exports.
def process_country(iso: str, dta_dir: Path, macc_dir: Path) -> dict[str, int]:

	# 1. Import of data & data cleaning. -------------------------------------------------------------------
	# Read .dta file via pyreadstat; output_format='dict' returns OrderedDict of Python lists.
	# np.array(lst, dtype=float) converts None (Stata missing) to NaN.
	all_rows_for_one_country: dict[str, np.ndarray] = {k: np.array(v, dtype=float) 
		for k, v in pyreadstat.read_dta(str(next((c for c in [dta_dir / iso, dta_dir / f"{iso}.dta", dta_dir / f"maps_{iso}.dta"] if c.is_file()), None)),
		output_format='dict', disable_datetime_conversion=True)[0].items()}
	N0 = len(next(iter(all_rows_for_one_country.values())))

	# Some country files have an integer row identifier pxl_id, but not all. This code ignores pxl_id and generates its own id.
	all_rows_for_one_country["id"] = np.arange(1, N0 + 1, dtype=float)

	# Data cleaning: ensure required columns exist with sensible defaults.
	for col in ["griscom", "brancalion", "bastin", "walker", "nr_buffer", "pl_buffer"]:
		if col not in all_rows_for_one_country: all_rows_for_one_country[col] = np.zeros(N0)
		else: all_rows_for_one_country[col] = np.where(np.isnan(all_rows_for_one_country[col]), 0.0, all_rows_for_one_country[col])
	for col in ["biomes", "nr_A", "type"]:
		if col not in all_rows_for_one_country: all_rows_for_one_country[col] = np.full(N0, np.nan)
	for col in ["nr_cost", "ep_cost", "np_cost", "crop_va"]:
		if col not in all_rows_for_one_country: all_rows_for_one_country[col] = np.zeros(N0)
	if "fao_ecoz" not in all_rows_for_one_country: all_rows_for_one_country["fao_ecoz"] = np.full(N0, np.nan)

	# Drop deserts/xeric (biomes=13) and mangroves (biomes=14); drop missing nr_A and type.
	all_rows_for_one_country = {k: v[(all_rows_for_one_country["biomes"] != 13) & (all_rows_for_one_country["biomes"] != 14) & ~np.isnan(all_rows_for_one_country["nr_A"]) & ~np.isnan(all_rows_for_one_country["type"])] for k, v in all_rows_for_one_country.items()}
	N = len(all_rows_for_one_country["id"])
	print("Number valid pixels is ", N)

	if RUN_ONLY_ONE_PIXEL:
		all_rows_for_one_country = {k: v[all_rows_for_one_country["id"] == float(TARGET_PIXEL_ID)] for k, v in all_rows_for_one_country.items()}
		N = len(all_rows_for_one_country["id"])
	# THIS DOESN'T LOOK RIGHT
	if N == 0: print(f"[SKIP] {iso}: no matching pixels.", flush=True); return {}

	# Convert 2011 establishment costs to 2020 USD. JFR: Note that the Busch et al code does not convert crop_va to 2020 USD, perhaps a mistake.
	for c in ["nr_cost", "ep_cost", "np_cost"]: all_rows_for_one_country[c] = all_rows_for_one_country[c] * 1.1577

	# Truncate negative A and k values at zero before growth calculations. JFR: a negative k implies growth without bound. Negative A implies growth toward a negative value.
	for col in ["nr_A", "nr_k"] + [f"{g}_{s}" for g in GENUS_ORDER for s in ["A", "k"]]:
		if col in all_rows_for_one_country: all_rows_for_one_country[col][all_rows_for_one_country[col] < 0] = 0.0

	# Plantation genus mapping.
	genus_arr = np.array([GENUS_BY_TYPE.get(t, "") for t in all_rows_for_one_country["type"].astype(int)], dtype=object)
	all_rows_for_one_country["genus"] = genus_arr
	all_rows_for_one_country["genus_A"] = np.full(N, np.nan)
	all_rows_for_one_country["genus_k"] = np.full(N, np.nan)
	for g in GENUS_ORDER:
		mg = genus_arr == g
		if np.any(mg):
			all_rows_for_one_country["genus_A"][mg] = all_rows_for_one_country[f"{g}_A"][mg]
			all_rows_for_one_country["genus_k"][mg] = all_rows_for_one_country[f"{g}_k"][mg]

	# Convert area m² → ha.
	all_rows_for_one_country["area_ha"] = all_rows_for_one_country["area_m2"] / 10000.0

	# Root-shoot ratios (IPCC 2019 V4 Ch4 Table 4.4).
	all_rows_for_one_country["nr_rootshoot"] = np.full(N, 0.26)
	all_rows_for_one_country["pl_rootshoot"] = np.full(N, 0.26)
	continent = get_continent(iso)
	is_brde_nede = np.isin(genus_arr, ["brde", "nede"])
	is_pinu_cunn_brev_neev = np.isin(genus_arr, ["pinu", "cunn", "brev", "neev"])

	for (eco, cont), value in NR_ROOTSHOOT_RULES.items():
		if cont == continent: all_rows_for_one_country["nr_rootshoot"][all_rows_for_one_country["fao_ecoz"] == eco] = value
	for (eco, cont), value in PL_ROOTSHOOT_RULES.items():
		if cont == continent: all_rows_for_one_country["pl_rootshoot"][all_rows_for_one_country["fao_ecoz"] == eco] = value

	if continent == "AMER":
		all_rows_for_one_country["nr_rootshoot"][(all_rows_for_one_country["fao_ecoz"] == 31) & is_brde_nede] = 0.466
		all_rows_for_one_country["nr_rootshoot"][(all_rows_for_one_country["fao_ecoz"] == 31) & is_pinu_cunn_brev_neev] = 0.337
		all_rows_for_one_country["nr_rootshoot"][(all_rows_for_one_country["fao_ecoz"] == 32) & is_pinu_cunn_brev_neev] = 0.237
		all_rows_for_one_country["pl_rootshoot"][(all_rows_for_one_country["fao_ecoz"] == 31) & is_pinu_cunn_brev_neev] = 0.203
		all_rows_for_one_country["pl_rootshoot"][(all_rows_for_one_country["fao_ecoz"] == 32) & is_pinu_cunn_brev_neev] = 0.237
	elif continent == "ASIA":
		all_rows_for_one_country["nr_rootshoot"][(all_rows_for_one_country["fao_ecoz"] == 31) & is_brde_nede] = 0.225
		all_rows_for_one_country["nr_rootshoot"][(all_rows_for_one_country["fao_ecoz"] == 31) & is_pinu_cunn_brev_neev] = 0.243
		all_rows_for_one_country["nr_rootshoot"][(all_rows_for_one_country["fao_ecoz"] == 32) & is_brde_nede] = 0.225
		all_rows_for_one_country["nr_rootshoot"][(all_rows_for_one_country["fao_ecoz"] == 32) & is_pinu_cunn_brev_neev] = 0.243
		all_rows_for_one_country["pl_rootshoot"][(all_rows_for_one_country["fao_ecoz"] == 31) & is_brde_nede] = 0.307
		all_rows_for_one_country["pl_rootshoot"][(all_rows_for_one_country["fao_ecoz"] == 31) & is_pinu_cunn_brev_neev] = 0.224
		all_rows_for_one_country["pl_rootshoot"][(all_rows_for_one_country["fao_ecoz"] == 32) & is_brde_nede] = 0.307
		all_rows_for_one_country["pl_rootshoot"][(all_rows_for_one_country["fao_ecoz"] == 32) & is_pinu_cunn_brev_neev] = 0.224

	# Country-level scalars stored as full arrays.
	all_rows_for_one_country["wood_storage_share"]      = np.full(N, mapped_value(WOOD_PRODUCT_STORAGE_MAP, iso))
	all_rows_for_one_country["p"]      = np.full(N, mapped_value(WOOD_PRODUCT_REVENUE_MAP, iso))
	all_rows_for_one_country["exotic"] = np.full(N, mapped_value(EXOTIC_SHARE_MAP, iso))
	all_rows_for_one_country["native"] = 1.0 - all_rows_for_one_country["exotic"]
	all_rows_for_one_country["nr_npv_estcost_USD_per_ha"] = all_rows_for_one_country["nr_cost"].copy()
	
	# 2. Calculations. -------------------------------------------------------------------
	# 2.1 Natural regeneration calculations.
	nr_k         = all_rows_for_one_country.get("nr_k", np.zeros(N))
	nr_A         = all_rows_for_one_country["nr_A"]
	nr_rootshoot = all_rows_for_one_country["nr_rootshoot"]
	years        = np.arange(200)[:, np.newaxis]   # (200,1) broadcasts over N pixels
	carbon_schedule = 2*nr_A*nr_k*(AGB_C_POOL + BGB_C_POOL*nr_rootshoot)*(np.exp(-nr_k*years) - np.exp(-2*nr_k*years)) + SOIL_C_POOL*NATURAL_SOIL_C
	carbon_wpt   = get_carbon_wpt() # Warming in year t from a megaton release of carbon in year 0.
	lengths      = np.arange(20, 120, 5)
	# Warming in year t from a mhectare planting in year 0.
	warming      = {T: (carbon_wpt[T::-1, np.newaxis] * carbon_schedule[:T+1]).sum(axis=0) for T in lengths}
	bid          = {T: all_rows_for_one_country["nr_cost"] + all_rows_for_one_country["crop_va"] * (1.0 - (1.0 + DISCOUNT_RATE) ** (-T)) / DISCOUNT_RATE for T in lengths}
	# Find the contract length with cheapest $/(cooling °C) for natural regeneration.
	len_idx     = {int(T): i for i, T in enumerate(lengths)}
	score_arr   = np.stack([bid[T] / warming[T] for T in lengths])  # (n_lengths, N)
	np.nan_to_num(score_arr, nan=np.inf, copy=False)  # NaN bid/warming → inf; never chosen as best
	nr_idx      = np.argmin(score_arr, axis=0)          # (N,) index into lengths
	nr_best_contract_length = lengths[nr_idx]
	best_option = np.full(N, "NR", dtype=object)
	best_length = nr_best_contract_length.astype(float)
	best_score  = score_arr[nr_idx, np.arange(N)]
	best_bid    = np.stack([bid[T] for T in lengths])[nr_idx, np.arange(N)]
	best_cs     = carbon_schedule.copy()  # (200, N); updated where plantation wins

	#2.2 Plantation calculations.
	all_rows_for_one_country["pl_best_contract_length"] = np.full(N, np.nan)
	for g in GENUS_ORDER:
		mask_g = genus_arr == g
		if not np.any(mask_g): continue
		A_g  = all_rows_for_one_country[f"{g}_A"][mask_g]
		k_g  = all_rows_for_one_country[f"{g}_k"][mask_g]
		rs_g = all_rows_for_one_country["pl_rootshoot"][mask_g]
		carbon_schedule_g = 2*A_g*k_g*(AGB_C_POOL + BGB_C_POOL*rs_g)*(np.exp(-k_g*years) - np.exp(-2*k_g*years)) + SOIL_C_POOL*PLANTATION_SOIL_C
		warming_g = {T: (carbon_wpt[T::-1, np.newaxis] * carbon_schedule_g[:T+1]).sum(axis=0) for T in lengths}
		if g == "cunn": est_cost = all_rows_for_one_country["np_cost"][mask_g] if iso == "CHN" else all_rows_for_one_country["ep_cost"][mask_g]
		elif g == "euca": est_cost = all_rows_for_one_country["ep_cost"][mask_g]
		else: est_cost = all_rows_for_one_country["exotic"][mask_g] * all_rows_for_one_country["ep_cost"][mask_g] + all_rows_for_one_country["native"][mask_g] * all_rows_for_one_country["np_cost"][mask_g]
		bid_g    = {T: est_cost + all_rows_for_one_country["crop_va"][mask_g] * (1.0 - (1.0 + DISCOUNT_RATE) ** (-T)) / DISCOUNT_RATE
			- A_g * (1.0 - np.exp(-k_g * T)) ** 2 / (1.0 + DISCOUNT_RATE) ** T for T in lengths}
		score_g  = np.stack([bid_g[T] / warming_g[T] for T in lengths])   # (n_lengths, Ng)
		np.nan_to_num(score_g, nan=np.inf, copy=False)
		pl_idx   = np.argmin(score_g, axis=0)
		pl_len   = lengths[pl_idx]
		all_rows_for_one_country["pl_best_contract_length"][mask_g] = pl_len
		gi       = np.arange(carbon_schedule_g.shape[1])
		pl_score = score_g[pl_idx, gi]
		pl_bid   = np.stack([bid_g[T] for T in lengths])[pl_idx, gi]
		better   = pl_score < best_score[mask_g]
		best_option[mask_g] = np.where(better, g, best_option[mask_g])
		best_length[mask_g] = np.where(better, pl_len.astype(float), best_length[mask_g])
		best_score[mask_g]  = np.where(better, pl_score, best_score[mask_g])
		best_bid[mask_g]    = np.where(better, pl_bid, best_bid[mask_g])
		best_cs[:, np.flatnonzero(mask_g)[better]] = carbon_schedule_g[:, better]

	# 3. Output ---------------------------------------------------------------------------------
	# Drop pixels where no valid option was found (best_score = inf means all bids were NaN).
	valid = np.isfinite(best_score)
	if not np.all(valid):
		# n_skip = int((~valid).sum())
		# print(f"  {iso}: {n_skip} pixel(s) with no valid option excluded.", flush=True)
		best_option = best_option[valid]; best_length = best_length[valid]
		best_score  = best_score[valid];  best_bid    = best_bid[valid]
		best_cs     = best_cs[:, valid]
		for key in list(all_rows_for_one_country.keys()): all_rows_for_one_country[key] = all_rows_for_one_country[key][valid]
		N = int(valid.sum())
	if N == 0: return {}
	macc_dir.mkdir(parents=True, exist_ok=True)
	if SKIP_SQLITE_EXPORT: print(f"[SKIP_SQLITE_EXPORT] {iso}", flush=True)
	else:
		with sqlite3.connect(macc_dir / SMDAMAGE_DB_FILE) as conn:
			conn.execute("PRAGMA synchronous = OFF")
			conn.execute("PRAGMA cache_size = -131072")
			ids = [int(x) for x in all_rows_for_one_country["id"]]
			conn.executemany(f"INSERT INTO {SMDAMAGE_PIXEL_TABLE} VALUES (?,?,?,?,?,?,?,?)",
				zip([iso]*N, ids, best_option.tolist(), best_length.astype(int).tolist(), best_bid.tolist(),
					best_score.astype(int).tolist(), all_rows_for_one_country["area_ha"].tolist(), all_rows_for_one_country["crop_va"].tolist()))
			conn.executemany(f"INSERT INTO {SMDAMAGE_YEAR_TABLE} VALUES (?,?,?,?)", ((iso, ids[i], t, float(best_cs[t, i])) for i in range(N) for t in range(int(best_length[i]))))
	print(f"[OK] {iso} {f'pixel {TARGET_PIXEL_ID}' if RUN_ONLY_ONE_PIXEL else 'all pixels'}", flush=True)
	return

