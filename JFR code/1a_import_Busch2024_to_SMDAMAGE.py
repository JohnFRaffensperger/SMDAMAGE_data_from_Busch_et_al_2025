@functools.lru_cache(maxsize=1)
def get_carbon_wpt() -> np.ndarray:
	"""Impulse response: warming (thousandths °C per MtC) for 296 years. Cached."""
	conn = sqlite3.connect(SMDAMAGE_DATA_DB)
	row  = conn.execute("SELECT * FROM warming_factors WHERE hector_name = 'ffi_emissions'").fetchone()
	conn.close()
	emission_factor = row[2]  # usually 1.0
	# row[4:] are year_001..year_296 in degrees/GtC; *1000 → thousandths/GtC; /1000 → thousandths/MtC
	return np.array([float(row[4 + t]) / emission_factor for t in range(296)])

