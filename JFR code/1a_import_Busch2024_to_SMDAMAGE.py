# 1a_import_Busch2024_to_SMDAMAGE.py — Written by JFR and CoPilot (mostly Claude), 2026-07-14.
# Purpose: Busch2024_to_SMDAMAGE.sqlite is the data bridge between the Busch 2024 paper ".dta" files and SMDAMAGE.
# Each pixel in each .dta file is treated as a bidder. Each bidder chooses a most profitable crop
# (natural regeneration or some plantation crop), the associated carbon removal schedule and a bid price to the SMDAMAGE auction.
# But 58 million bidders is too big for SMDAMAGE, so I use a k-means to cluster similar bidders into groups.
# Later in the workflow, SMDAMAGE project file create_database.py reads Busch2024_to_SMDAMAGE.sqlite
# and loads forestry bidders, one per group, into the SMDAMAGE auction.

# Origin: I adapted this code from "1. Model loop all data corrected.do", the Stata pipeline published by Busch et al,
# "Cost-effectiveness of natural forest regeneration and plantations for climate mitigation" (2024).
# That Stata script reads per-country .dta input files, applies biome and geographic filters,
# computes Chapman-Richards carbon growth curves under natural regeneration and plantation scenarios,
# applies IPCC root-to-shoot ratios and FAO ecozone rules, evaluates cost-effectiveness per pixel
# in $/tCO2, and selects the least-cost reforestation option for each pixel.

# This version replaces the Busch et al harvest-year selection with a bid_score approach.
# For each pixel, the winning option and contract length minimise bid / warming ($/°C cooling),
# where bid = establishment_cost + discounted_opportunity_cost (- discounted_harvest_NPV for plantations)
# and warming = convolution of annual carbon sequestration with the Hector climate impulse response.
 
# Input:
#   Input/*.dta published by Busch et al as the replication dataset for the 2024 paper.
#   Not generated by any script in this repo; download from the paper’s data repository.
#   Each .dta file contains pixel information on one country. One row per pixel with
#   Chapman-Richards growth parameters (A, k) for natural regeneration and each plantation
#   genus, establishment costs, crop opportunity value, area, biome, FAO ecozone, and
#   geographic screen variables (griscom, brancalion, bastin, walker).
#
# Output: Output/Databases/Busch2024_to_SMDAMAGE.sqlite, two tables:
#   Undiscounted_dta_output, one row per pixel:
#     country, pixel_id, option (NR or plantation genus), best_contract_length,
#     bid (USD/ha at best_contract_length), bid_score (int, bid/warming at best_contract_length),
#     area_ha, crop_va_USD_per_ha_per_year.
#   tC_per_h_per_year, one row per pixel per year (year 0 … best_contract_length-1):
#     country, pixel_id, year, tC_per_ha_per_year.
#
# Downstream pipeline (later scripts add further tables to the same SQLite file):
#   2_k_means_carbon_removal.py — clusters pixels on their carbon profiles within each contract
#     length; writes cluster centre schedules (carbon_removal_schedules) and assigns each pixel
#     a cluster_index (added as a column on Undiscounted_dta_output).
#   3_import_k_means_csv_to_sqlite.py — loads k-means CSV results into the database.
#   4_move_Busch_data_to_SMDAMAGE.py — computes per-pixel NPV bid costs and builds area-share
#     bid-step supply curves (forestry_bid_curves), one curve per (contract_length, cluster_index).

